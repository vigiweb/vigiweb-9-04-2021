package servlet;

import controlador.usuariosDAO;
import java.io.IOException;
import java.io.PrintWriter;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import modelo.usuarios;

@WebServlet(name = "ConsultarUsuarios", urlPatterns = {"/ConsultarUsuarios"})
public class ConsultarUsuarios extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {

            String Accion = request.getParameter("Actualizar");
            System.out.println("Accion " + Accion);

            String Identificacion = request.getParameter("IdConsultado");

            usuariosDAO miUsuariodao = new usuariosDAO();
            usuarios usuario = null;

            usuario = miUsuariodao.ConsultarUsuario(Identificacion);

            if (Accion != null) {

                if (usuario != null) {

                    response.sendRedirect("/vigiwebB/vista/usuarios.jsp?Nombres=" + usuario.getNombre_usuario() + "&"
                            + "Contrasena=" + usuario.getContrasena() + "&"
                            + "Telefono=" + usuario.getTelefono() + "&"
                            + "Direccion=" + usuario.getDireccion() + "&"
                            + "Correo=" + usuario.getCorreo() + "&"
                            + "Barrio=" + usuario.getBarrio() + "&"
                            + "Estado_usuario=" + usuario.getEstado_usuario_id_estado_usuario() + "&"
                            + "Tipo_usuario=" + usuario.getTipo_usuario_id_tipo_usuario() + "&"
                            + "Tipo_doc=" + usuario.getTipo_documento_id_tipo_documento() + "&"
                            + "Genero=" + usuario.getTipo_genero_id_tipo_genero() + "&"
                            + "Ciudad=" + usuario.getCiudad_id_ciudad() + "&"
                            + "NumeroDoc=" + usuario.getNumero_documento() + "&"
                            + "Vista=" + "Actualizar" + "&");

                    System.out.println("Salio");

                } else {
                    out.println("<script type=\"text/javascript\">");
                    out.println("alert('" + "No se ha podido relizar la consulta." + "\n" + "Por favor verificar la identificacion: " + Identificacion + "');");
                    out.println("</script>");
                }

            } else {

                if (usuario != null) {

                    String respuestaRegistrada = miUsuariodao.EliminarUsuarios(usuario);
                    if (respuestaRegistrada.length() == 0) {
                        out.println("<script type=\"text/javascript\">");
                        out.println("alert('" + "Eliminacion Realizada." + "');");

                        out.println("window.location.href = '/vigiwebB/vista/usuarios.jsp';");
                        out.println("</script>");

                    } else {

                        out.println("<script type=\"text/javascript\">");
                        out.println("alert('" + "No se ha podido relizar la eliminacion." + respuestaRegistrada.replace("'", "") + "');");
                        out.println("</script>");
                    }
                } else {

                    out.println("<script type=\"text/javascript\">");
                    out.println("alert('" + "No se ha podido relizar la consulta." + "\n" + "Por favor verificar la identificacion: " + Identificacion + "');");
                    out.println("</script>");

                }
            }
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
